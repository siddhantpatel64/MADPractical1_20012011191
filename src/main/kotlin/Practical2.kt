fun main() {
    val x = 12
    println("Integer value is: $x")
    val x2: Double = x.toDouble()
    println("Double value is: $x2")

    val y = "12"
    println("String value is: $y")
    val y1: Int = y.toInt()
    val y2: Double = y.toDouble()
    println("Integer value is: $y1")
    println("Double value is: $y2")
}
